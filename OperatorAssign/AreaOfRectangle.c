#include <stdio.h>

int main() {

  // Create integer variables

  int length = 15;
  int width = 10;
  int area;

  // Calculate the area of a rectangle

  area = length * width;

  // Print the variables
  
  printf("Length is: %d\n", length);
  printf("Width is: %d\n", width);
  printf("Area of the rectangle is: %d", area);

  return 0;
}